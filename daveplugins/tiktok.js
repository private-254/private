const { ttdl } = require("ruhend-scraper");
const axios = require('axios');

// Store processed message IDs to prevent duplicates
const processedMessages = new Set();

let daveplug = async (m, { dave, reply, text }) => {
    try {
        // Check if message has already been processed
        if (processedMessages.has(m.key.id)) {
            return;
        }

        // Add message ID to processed set
        processedMessages.add(m.key.id);

        // Clean up old message IDs after 5 minutes
        setTimeout(() => {
            processedMessages.delete(m.key.id);
        }, 5 * 60 * 1000);

        if (!text) {
            return reply("Please provide a TikTok link for the video.\nExample: .tiktok https://tiktok.com/...");
        }

        // Extract URL from command
        const url = text.trim();

        // Check for various TikTok URL formats
        const tiktokPatterns = [
            /https?:\/\/(?:www\.)?tiktok\.com\//,
            /https?:\/\/(?:vm\.)?tiktok\.com\//,
            /https?:\/\/(?:vt\.)?tiktok\.com\//,
            /https?:\/\/(?:www\.)?tiktok\.com\/@/,
            /https?:\/\/(?:www\.)?tiktok\.com\/t\//
        ];

        const isValidUrl = tiktokPatterns.some(pattern => pattern.test(url));

        if (!isValidUrl) {
            return reply("That is not a valid TikTok link. Please provide a valid TikTok video link.");
        }

        await dave.sendMessage(m.chat, {
            react: { text: 'ðŸ”„', key: m.key }
        });

        try {
            // Try multiple APIs in sequence
            const apis = [
                `https://api.princetechn.com/api/download/tiktok?apikey=prince&url=${encodeURIComponent(url)}`,
                `https://api.princetechn.com/api/download/tiktokdlv2?apikey=prince_tech_api_azfsbshfb&url=${encodeURIComponent(url)}`,
                `https://api.princetechn.com/api/download/tiktokdlv3?apikey=prince_tech_api_azfsbshfb&url=${encodeURIComponent(url)}`,
                `https://api.princetechn.com/api/download/tiktokdlv4?apikey=prince_tech_api_azfsbshfb&url=${encodeURIComponent(url)}`,
                `https://api.dreaded.site/api/tiktok?url=${encodeURIComponent(url)}`
            ];

            let videoUrl = null;
            let audioUrl = null;
            let title = null;

            // Try each API until one works
            for (const apiUrl of apis) {
                try {
                    const response = await axios.get(apiUrl, { timeout: 10000 });

                    if (response.data) {
                        // Handle different API response formats
                        if (response.data.result && response.data.result.videoUrl) {
                            // PrinceTech API format
                            videoUrl = response.data.result.videoUrl;
                            audioUrl = response.data.result.audioUrl;
                            title = response.data.result.title;
                            break;
                        } else if (response.data.tiktok && response.data.tiktok.video) {
                            // Dreaded API format
                            videoUrl = response.data.tiktok.video;
                            break;
                        } else if (response.data.video) {
                            // Alternative format
                            videoUrl = response.data.video;
                            break;
                        }
                    }
                } catch (apiError) {
                    console.error(`TikTok API failed: ${apiError.message}`);
                    continue;
                }
            }

            // If no API worked, try the original ttdl method
            if (!videoUrl) {
                let downloadData = await ttdl(url);
                if (downloadData && downloadData.data && downloadData.data.length > 0) {
                    const mediaData = downloadData.data;
                    for (let i = 0; i < Math.min(20, mediaData.length); i++) {
                        const media = mediaData[i];
                        const mediaUrl = media.url;

                        // Check if URL ends with common video extensions
                        const isVideo = /\.(mp4|mov|avi|mkv|webm)$/i.test(mediaUrl) || 
                                      media.type === 'video';

                        if (isVideo) {
                            await dave.sendMessage(m.chat, {
                                video: { url: mediaUrl },
                                mimetype: "video/mp4",
                                caption: "_ð™³ð™°ðš…ð™´-ð™¼ð™³ is on fire ðŸ”¥_"
                            }, { quoted: m });
                        } else {
                            await dave.sendMessage(m.chat, {
                                image: { url: mediaUrl },
                                caption: "_ð™³ð™°ðš…ð™´-ð™¼ð™³ is on fire ðŸ”¥_"
                            }, { quoted: m });
                        }
                    }
                    return;
                }
            }

            // Send the video if we got a URL from the APIs
            if (videoUrl) {
                try {
                    // Download video as buffer
                    const videoResponse = await axios.get(videoUrl, {
                        responseType: 'arraybuffer',
                        timeout: 30000,
                        headers: {
                            'User-Agent': 'Mozilla/5.0 (Windows NT 10.0; Win64; x64) AppleWebKit/537.36 (KHTML, like Gecko) Chrome/91.0.4472.124 Safari/537.36'
                        }
                    });

                    const videoBuffer = Buffer.from(videoResponse.data);

                    const caption = title ? `_ð™³ð™°ðš…ð™´-ð™¼ð™³ is on fire ðŸ”¥_\n\nðŸ“ Title: ${title}` : "_ð™³ð™°ðš…ð™´-ð™¼ð™³ is on fireðŸ”¥_";

                    await dave.sendMessage(m.chat, {
                        video: videoBuffer,
                        mimetype: "video/mp4",
                        caption: caption
                    }, { quoted: m });

                    // If we have audio URL, download and send it as well
                    if (audioUrl) {
                        try {
                            const audioResponse = await axios.get(audioUrl, {
                                responseType: 'arraybuffer',
                                timeout: 30000,
                                headers: {
                                    'User-Agent': 'Mozilla/5.0 (Windows NT 10.0; Win64; x64) AppleWebKit/537.36 (KHTML, like Gecko) Chrome/91.0.4472.124 Safari/537.36'
                                }
                            });

                            const audioBuffer = Buffer.from(audioResponse.data);

                            await dave.sendMessage(m.chat, {
                                audio: audioBuffer,
                                mimetype: "audio/mp3",
                                caption: "ðŸŽµ Audio from TikTok"
                            }, { quoted: m });
                        } catch (audioError) {
                            console.error(`Failed to download audio: ${audioError.message}`);
                        }
                    }
                    return;
                } catch (downloadError) {
                    console.error(`Failed to download video: ${downloadError.message}`);
                    // Fallback to URL method
                    try {
                        const caption = title ? `_ð™³ð™°ðš…ð™´-ð™¼ð™³ is on fire ðŸ”¥_\n\nðŸ“ Title: ${title}` : "_ð™³ð™°ðš…ð™´-ð™¼ð™³ is on fire ðŸ”¥_";

                        await dave.sendMessage(m.chat, {
                            video: { url: videoUrl },
                            mimetype: "video/mp4",
                            caption: caption
                        }, { quoted: m });
                        return;
                    } catch (urlError) {
                        console.error(`URL method also failed: ${urlError.message}`);
                    }
                }
            }

            // If we reach here, no method worked
            return reply("âŒ Failed to download TikTok video. All download methods failed. Please try again with a different link or check if the video is available.");
        } catch (error) {
            console.error('Error in TikTok download:', error);
            reply("Failed to download the TikTok video. Please try again with a different link.");
        }
    } catch (error) {
        console.error('Error in TikTok command:', error);
        reply("An error occurred while processing the request. Please try again later.");
    }
};

daveplug.help = ['tiktok'];
daveplug.tags = ['download'];
daveplug.command = ['tiktok', 'tt'];

module.exports = daveplug;